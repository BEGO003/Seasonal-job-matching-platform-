/**
 * Splits a full phone number into its country code and the national number.
 * Finds the longest matching country code.
 * @param fullNumber The full phone number (e.g., "+201234567890", "00201234567890", "201234567890")
 * @returns { code: string, number: string }
 */
export const splitPhoneNumber = (fullNumber: string): { code: string; number: string; country?: string } => {
  if (!fullNumber) return { code: "", number: "", country: "" };
  
  // Normalize: remove leading + or 00
  let normalized = fullNumber;
  // if (normalized.startsWith("+")) normalized = normalized.substring(1);
  // else if (normalized.startsWith("00")) normalized = normalized.substring(2);

  // Sort codes by length (desc) to match longest code first
  const sortedCodes = [...countryCodes].sort((a, b) => b.code.length - a.code.length);

  for (const item of sortedCodes) {
    // Check if phone starts with this code
    if (normalized.startsWith(item.code)) {
      return {
        code: item.code,
        number: normalized.substring(item.code.length),
        country: item.country
      };
    }
  }

  // Fallback if no code matches
  return { code: "", number: fullNumber };
};
